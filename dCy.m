function [dC1y,dC21y,dC22y] = dCy(m0,m1,m11,m2,m22,m12,dm0y,dm1y,dm11y,dm2y,dm22y,dm12y)
dC1y=(dm11y*m22 - 2*dm12y*m12 + dm22y*m11)/(m0*(m11*m22 - m12^2) - m2^2*m11 - m1^2*m22 + 2*m1*m2*m12) -...
    ((m11*m22 - m12^2)*(m0*(dm11y*m22 - 2*dm12y*m12 + dm22y*m11) + dm0y*(m11*m22 - m12^2) - dm11y*m2^2 -...
    dm22y*m1^2 + 2*dm1y*m2*m12 + 2*dm2y*m1*m12 - 2*dm2y*m2*m11 + 2*dm12y*m1*m2 - 2*dm1y*m1*m22))/...
    (- m22*m1^2 + 2*m12*m1*m2 - m11*m2^2 + m0*(m11*m22 - m12^2))^2;
dC21y=((m2*m12 - m1*m22)*(m0*(dm11y*m22 - 2*dm12y*m12 + dm22y*m11) + dm0y*(m11*m22 - m12^2) - dm11y*m2^2 -...
    dm22y*m1^2 + 2*dm1y*m2*m12 + 2*dm2y*m1*m12 - 2*dm2y*m2*m11 + 2*dm12y*m1*m2 - 2*dm1y*m1*m22))/...
    (- m22*m1^2 + 2*m12*m1*m2 - m11*m2^2 + m0*(m11*m22 - m12^2))^2 - (dm2y*m12 + dm12y*m2 - dm1y*m22 -...
    dm22y*m1)/(m0*(m11*m22 - m12^2) - m2^2*m11 - m1^2*m22 + 2*m1*m2*m12);
dC22y=((m1*m12 - m2*m11)*(m0*(dm11y*m22 - 2*dm12y*m12 + dm22y*m11) + dm0y*(m11*m22 - m12^2) - dm11y*m2^2 -...
    dm22y*m1^2 + 2*dm1y*m2*m12 + 2*dm2y*m1*m12 - 2*dm2y*m2*m11 + 2*dm12y*m1*m2 - 2*dm1y*m1*m22))/...
    (- m22*m1^2 + 2*m12*m1*m2 - m11*m2^2 + m0*(m11*m22 - m12^2))^2 - (dm1y*m12 - dm2y*m11 - dm11y*m2 +...
    dm12y*m1)/(m0*(m11*m22 - m12^2) - m2^2*m11 - m1^2*m22 + 2*m1*m2*m12);
end

