function [dC1x,dC21x,dC22x] = dCx(m0,m1,m11,m2,m22,m12,dm0x,dm1x,dm11x,dm2x,dm22x,dm12x)
dC1x=(dm11x*m22 - 2*dm12x*m12 + dm22x*m11)/(m0*(m11*m22 - m12^2) - m2^2*m11 -...
    m1^2*m22 + 2*m1*m2*m12) - ((m11*m22 - m12^2)*(m0*(dm11x*m22 - 2*dm12x*m12 +...
    dm22x*m11) + dm0x*(m11*m22 - m12^2) - dm11x*m2^2 - dm22x*m1^2 + 2*dm1x*m2*m12 +...
    2*dm2x*m1*m12 - 2*dm2x*m2*m11 + 2*dm12x*m1*m2 - 2*dm1x*m1*m22))/(- m22*m1^2 +...
    2*m12*m1*m2 - m11*m2^2 + m0*(m11*m22 - m12^2))^2;
dC21x=((m2*m12 - m1*m22)*(m0*(dm11x*m22 - 2*dm12x*m12 + dm22x*m11) + dm0x*(m11*m22 - m12^2) -...
    dm11x*m2^2 - dm22x*m1^2 + 2*dm1x*m2*m12 + 2*dm2x*m1*m12 - 2*dm2x*m2*m11 + 2*dm12x*m1*m2 -...
    2*dm1x*m1*m22))/(- m22*m1^2 + 2*m12*m1*m2 - m11*m2^2 + m0*(m11*m22 - m12^2))^2 -...
    (dm2x*m12 + dm12x*m2 - dm1x*m22 - dm22x*m1)/(m0*(m11*m22 - m12^2) - m2^2*m11 - m1^2*m22 + 2*m1*m2*m12);
dC22x=((m1*m12 - m2*m11)*(m0*(dm11x*m22 - 2*dm12x*m12 + dm22x*m11) + dm0x*(m11*m22 - m12^2) -...
    dm11x*m2^2 - dm22x*m1^2 + 2*dm1x*m2*m12 + 2*dm2x*m1*m12 - 2*dm2x*m2*m11 + 2*dm12x*m1*m2 -...
    2*dm1x*m1*m22))/(- m22*m1^2 + 2*m12*m1*m2 - m11*m2^2 + m0*(m11*m22 - m12^2))^2 -...
    (dm1x*m12 - dm2x*m11 - dm11x*m2 + dm12x*m1)/(m0*(m11*m22 - m12^2) - m2^2*m11 - m1^2*m22 + 2*m1*m2*m12);
end

